<?php
    // An integer number:
    $value = 123;
    echo $value, "=> type ", gettype($value),"\n";
    // A text:
    $value = "text";
    echo $value,"=> type ", gettype($value),"\n";
    // A real number:
    $value = 12.3;
    echo $value," => type ", gettype($value),"\n";
    // The null reference:
    echo $VALUE," => type ", gettype($VALUE),"\n";

?>